package class_design;

/*
 * we will learn : 
 * -what are abstract methods
 * -how to use abstract methods
 */

public class abstract_methods {
	
	/*
	 * Abstract Method Definition Rules : 
	 * 
	 * 1) Abstract method can only be defined in abstract classes
	 * 
	 * 2) Abstract method can not be declared private or final
	 * 
	 * 3) Abstract methods must not provide a method body/implementation in the
	 * abstract class in which it is declared
	 * 
	 * 4) Implementing abstract method in subclass follows the same rules for overriding 
	 * a method, e.g method signature must be the same and access modifier needs to be
	 * same or higher
	 */

}
